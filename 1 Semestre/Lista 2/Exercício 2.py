#Perguntando quantos dolares o usuario gostaria de comprar
Quantidade = float(input('Digite quantos Dólares tu gostaria de comprar: '))

#Fazendo a converção em Reais
Reais = Quantidade * 4.95

#Mostrando ao usuário a converção de Dolar para Real
print('U$',Quantidade,'Dólares equivale a R$ ', Reais,'Reais')